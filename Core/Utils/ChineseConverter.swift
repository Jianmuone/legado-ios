    func detectType(_ text: String) -> ChineseTextType {
        var simplifiedCount = 0
        var traditionalCount = 0
        
        for char in text {
            if simplifiedToTraditional[char] != nil && simplifiedToTraditional[char] != char {
                simplifiedCount += 1
            }
            if traditionalToSimplified[char] != nil && traditionalToSimplified[char] != char {
                traditionalCount += 1
            }
        }
        
        return traditionalCount > simplifiedCount ? .traditional : .simplified
